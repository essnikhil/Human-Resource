    private void jBtnFinalUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnFinalUpdateActionPerformed
        int select=jUTable.getSelectedRow();
        DefaultTableModel model=(DefaultTableModel) jUTable.getModel();
        Employee emp=(Employee) model.getValueAt(select,0);
        
        emp.setName(jTxtName.getText());
        emp.setEid(jTxtEmpId.getText());
        emp.setAge(jTxtAge.getText());
        emp.setGender(jTxtGender.getText());
        emp.setDate(jTxtDate.getText());
        emp.setLevel(jTxtLevel.getText());
        emp.setTeam_info(jTxtInfo.getText());
        emp.setTitle(jTxtPosTitle.getText());
        emp.setPhone(jTxtNum.getText());
        emp.setEmail(jTxtEmail.getText());
        
        JOptionPane.showMessageDialog(this, "Employee details updated successfully");
    }//GEN-LAST:event_jBtnFinalUpdateActionPerformed

    private void jUTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jUTableMouseClicked
        int select=jUTable.getSelectedRow();
        if(select<0){
            JOptionPane.showMessageDialog(this, "Please select an Employee");
            return;
        }
        
        DefaultTableModel model=(DefaultTableModel) jUTable.getModel();
        Employee emp=(Employee) model.getValueAt(select,0);
        
        jTxtName.setText(emp.getName());
        jTxtEmpId.setText(String.valueOf(emp.getEid()));
        jTxtAge.setText(String.valueOf(emp.getAge()));
        jTxtGender.setText(emp.getGender());
        jTxtDate.setText(emp.getDate());
        jTxtLevel.setText(emp.getLevel());
        jTxtInfo.setText(emp.getTeam_info());
        jTxtPosTitle.setText(emp.getTitle());
        jTxtNum.setText(String.valueOf(emp.getPhone()));
        jTxtEmail.setText(emp.getEmail());
    }//GEN-LAST:event_jUTableMouseClicked


