    private void populateTabel() {
        DefaultTableModel model=(DefaultTableModel) jUTable.getModel();
        model.setRowCount(0);
        for(Employee e:emplst.getEmp_list()){
            Object[] col=new Object[5];
            col[0]=e;
            col[1]=e.getName();
            col[2]=e.getTitle();
            col[3]=e.getPhone();
            col[4]=e.getEmail();
            
            model.addRow(col);
        }
        
    }
